            <div class="font-weight-bold">
            <pre>
                    INSERTION-SORT (A)
                     for j = 2 to A:length
                     key = A[j]
                     // Insert A[j] into the sorted sequence A[1 .. j-1].
                     i = j - 1
                     while i > 0 and A[i] > key
                     A[i+1] = A[j]
                     i = i - 1
                     A[i+1] = key
            </pre>
            </div>

