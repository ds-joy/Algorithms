    void insertionSort(int array[], int size) {
        for (int step = 1; step < size; step++) {
        int key = array[step];
        int j = step - 1;
        while (key < array[j] && j >= 0) {
            // For descending order, change key<array[j] to key>array[j].
            array[j + 1] = array[j];
            --j;
            }
        array[j + 1] = key;
        }
    }
    void printArray(int array[], int size) {
        for (int i = 0; i < size; i++) {
        cout << array[i] << " ";
        }
        cout << endl;
    }
                            
